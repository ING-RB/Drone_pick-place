% mlreportgen.dom.TableHeader Header section of a formal table
%
%    TableHeader properties:
%      Children                - Children (rows) of this table header
%      ColSep                  - Type of line to be drawn between columns
%      ColSepColor             - Color of this header's column separator
%      ColSepWidth             - Width of this header's column separator
%      Id                      - Id of this header
%      NCols                   - Number of columns in this table header
%      NRows                   - Number of rows in this table header
%      RowSep                  - Type of line to be drawn between rows
%      RowSepColor             - Color of this header's row separator
%      RowSepWidth             - Width of this header's row separator
%      Parent                  - Parent of this header (formal table)
%      Style                   - Formats that apply to this header
%      StyleName               - Name of header style defined in template
%      TableEntriesHAlign      - Default horizontal alignment of entries
%      TableEntriesInnerMargin - Inner margin (padding) of table's entries
%      TableEntriesStyle       - Style of header's entries
%      TableEntriesVAlign      - Vertical alignment of header entries
%      Tag                     - Tag of this header
%
%    TableHeader methods:
%      append - Append a row of table entries to this header.
%      entry  - Get a header entry
%      row    - Get a header row
