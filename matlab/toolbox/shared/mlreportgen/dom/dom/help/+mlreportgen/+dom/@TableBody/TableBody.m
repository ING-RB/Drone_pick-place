%      RowSepWidth             - Width of this body's row separator
%      Parent                  - Parent of this body (formal table)
%      Style                   - Formats that apply to this body
%      StyleName               - Name of body style defined in template
%      TableEntriesHAlign      - Default horizontal alignment of entries
%      TableEntriesInnerMargin - Inner margin (padding) of table's entries
%      TableEntriesStyle       - Style of body's entries
%      TableEntriesVAlign      - Vertical alignment of body entries
%      Tag                     - Tag of this body
%
%    TableBody methods:
%      append - Append a row of table entries to this body.
