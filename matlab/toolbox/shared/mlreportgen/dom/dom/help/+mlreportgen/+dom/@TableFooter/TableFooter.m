% mlreportgen.dom.TableFooter Footer section of a formal table
%
%    TableFooter properties:
%      Children                - Children (rows) of this table footer
%      ColSep	               - Type of line to be drawn between columns
%      ColSepColor             - Color of this footer's column separator
%      ColSepWidth             - Width of this footer's column separator
%      Id                      - Id of this footer
%      NCols                   - Number of columns in this table footer
%      NRows                   - Number of rows in this table footer
%      RowSep                  - Type of line to be drawn between rows
%      RowSepColor             - Color of this footer's row separator
%      RowSepWidth             - Width of this footer's row separator
%      Parent                  - Parent of this footer (formal table)
%      Style                   - Formats that apply to this footer
%      StyleName               - Name of footer style defined in template
%      TableEntriesHAlign      - Default horizontal alignment of entries
%      TableEntriesInnerMargin - Inner margin (padding) of table's entries
%      TableEntriesStyle       - Style of footer's entries
%      TableEntriesVAlign      - Vertical alignment of footer entries
%      Tag                     - Tag of this footer
%
%    TableFooter methods:
%      append - Append a row of table entries to this footer.
%      entry  - Get a footer entry
%      row    - Get a footer row
