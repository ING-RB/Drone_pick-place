%mlreportgen.ppt.TemplateTable Table from the template presentation
%    Specifies a table parsed from the template presentation. In the PPT
%    API, when the template presentation slide contains a table, the API
%    creates a TemplateTable object. Use the find method with the slide
%    object to find the template table object. Use the replace method to
%    replace it with another PPT API Table object.
%
%    TemplateTable properties:
%        XMLMarkup  - XML markup of template table
%        Name       - Template table name
%        X          - Upper-left x-coordinate position of template table
%        Y          - Upper-left y-coordinate position of template table
%        Width      - Width of template table
%        Height     - Height of template table
%        Style      - Template table formatting
%        Children   - Children of this PPT API object
%        Parent     - Parent of this PPT API object
%        Id         - ID for this PPT API object
%        Tag        - Tag for this PPT API object
%
%    TemplateTable methods:
%        replace    - Replace template table
%
%    See also mlreportgen.ppt.Table

