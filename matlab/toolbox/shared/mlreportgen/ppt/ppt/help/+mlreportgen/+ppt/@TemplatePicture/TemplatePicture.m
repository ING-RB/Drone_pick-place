%mlreportgen.ppt.TemplatePicture Picture from the template presentation
%    Specifies a picture parsed from the template presentation. In the PPT
%    API, when the template presentation slide contains a picture, the API
%    creates a TemplatePicture object. Use the find method with the slide
%    object to find the template picture object. Use the replace method to
%    replace it with another PPT API Picture object.
%
%    TemplatePicture properties:
%        XMLMarkup  - XML markup of template picture
%        Name       - Template picture name
%        X          - Upper-left x-coordinate position of template picture
%        Y          - Upper-left y-coordinate position of template picture
%        Width      - Width of template picture
%        Height     - Height of template picture
%        Style      - Template picture formatting
%        Children   - Children of this PPT API object
%        Parent     - Parent of this PPT API object
%        Id         - ID for this PPT API object
%        Tag        - Tag for this PPT API object
%
%    TemplatePicture methods:
%        replace    - Replace template picture
%
%    See also mlreportgen.ppt.Picture

