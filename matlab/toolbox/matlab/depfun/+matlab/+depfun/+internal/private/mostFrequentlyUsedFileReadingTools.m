                                {'.xls' '.xlsx' '.xlsm' '.xltx' '.xltm'} ... % xlsread
                                {} ... % xmlread
                                {} ... % xslt
                              });
        if matlab.depfun.internal.requirementsConstants.isSimulinkCompilerAccessible
            mdl_ext = matlab.depfun.internal.requirementsConstants.simulinkModelExt;
            list('Simulink.SimulationInput') = mdl_ext;                    % Simulink.SimulationInput
            list('SimulationInput') = mdl_ext;                             % In case Simulink.SimulationInput is imported.
